function nextQuestion() {
    if (userAnswers[currentQuestion] === undefined) {
        alert(translations[currentLanguage]['quiz.alert']);
        return;
    }
    
    currentQuestion++;
    displayQuestion();
}

function prevQuestion() {
    currentQuestion--;
    displayQuestion();
}

function showResults() {
    const quizContent = document.getElementById('quiz-content');
    const results = document.getElementById('results');
    
    if (quizContent) quizContent.style.display = 'none';
    if (results) results.style.display = 'block';
}

// Initialize the page
document.addEventListener('DOMContentLoaded', function() {
    // Language dropdown functionality
    const languageSwitcher = document.querySelector('.language-switcher');
    const languageDropdown = document.querySelector('.language-dropdown');
    
    if (languageSwitcher) {
        languageSwitcher.addEventListener('click', function(e) {
            // Prevent closing when clicking on the dropdown
            if (e.target.closest('.language-dropdown')) return;
            
            if (languageDropdown) {
                languageDropdown.style.display = languageDropdown.style.display === 'block' ? 'none' : 'block';
            }
        });
    }
    
    // Close dropdown when clicking outside
    document.addEventListener('click', function(e) {
        if (languageSwitcher && !languageSwitcher.contains(e.target)) {
            if (languageDropdown) {
                languageDropdown.style.display = 'none';
            }
        }
    });
    
    // Handle language selection
    if (languageDropdown) {
        document.querySelectorAll('.language-dropdown a').forEach(link => {
            link.addEventListener('click', function(e) {
                e.preventDefault();
                const lang = this.textContent.toLowerCase();
                if (lang === 'english') translatePage('en');
                else if (lang === 'español') translatePage('es');
                else if (lang === 'français') translatePage('fr');
                else if (lang === 'deutsch') translatePage('de');
                
                if (languageDropdown) {
                    languageDropdown.style.display = 'none';
                }
            });
        });
    }
    
    // Header scroll effect
    window.addEventListener('scroll', function() {
        const header = document.getElementById('header');
        if (header) {
            if (window.scrollY > 50) {
                header.classList.add('scrolled');
            } else {
                header.classList.remove('scrolled');
            }
        }
    });

    // Coach filtering functionality
    const filterButtons = document.querySelectorAll('.filter-btn');
    const coachCards = document.querySelectorAll('.coach-card');
    
    filterButtons.forEach(button => {
        button.addEventListener('click', () => {
            // Remove active class from all buttons
            filterButtons.forEach(btn => btn.classList.remove('active'));
            // Add active class to clicked button
            button.classList.add('active');
            
            const filterValue = button.getAttribute('data-filter');
            
            coachCards.forEach(card => {
                if (filterValue === 'all' || card.getAttribute('data-category') === filterValue) {
                    card.style.display = 'block';
                } else {
                    card.style.display = 'none';
                }
            });
        });
    });

    // Initialize quiz if on quiz page
    if (document.getElementById('quiz-content')) {
        displayQuestion();
    }

    // Smooth scrolling for anchor links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function(e) {
            e.preventDefault();
            
            const targetId = this.getAttribute('href');
            if (targetId === '#') return;
            
            const targetElement = document.querySelector(targetId);
            if (targetElement) {
                window.scrollTo({
                    top: targetElement.offsetTop - 80,
                    behavior: 'smooth'
                });
            }
        });
    });
    
    // Initialize with English as default
    translatePage('en');
});