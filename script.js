// Quiz functions
function displayQuestion() {
    const quizContent = document.getElementById('quiz-content');
    const quizProgress = document.getElementById('quiz-progress');
    const translation = translations[currentLanguage];
    
    if (!quizContent) return;
    
    if (currentQuestion >= getQuizQuestions().length) {
        showResults();
        return;
    }
    
    const question = getQuizQuestions()[currentQuestion];
    const progressPercentage = (currentQuestion / getQuizQuestions().length) * 100;
    
    quizProgress.style.width = `${progressPercentage}%`;
    
    let optionsHtml = '';
    question.options.forEach((option, index) => {
        optionsHtml += `
            <div class="option" data-index="${index}">${option}</div>
        `;
    });
    
    quizContent.innerHTML = `
        <div class="question">
            <h3>${question.question}</h3>
            <div class="options">${optionsHtml}</div>
        </div>
        <div class="quiz-navigation">
            ${currentQuestion > 0 ? `<button class="btn btn-outline" id="prev-btn">${translation['quiz.prev']}</button>` : '<div></div>'}
            <button class="btn" id="next-btn">${currentQuestion === getQuizQuestions().length - 1 ? translation['quiz.seeResults'] : translation['quiz.next']}</button>
        </div>
    `;
    
    // Add event listeners to options
    document.querySelectorAll('.option').forEach(option => {
        option.addEventListener('click', function() {
            document.querySelectorAll('.option').forEach(opt => opt.classList.remove('selected'));
            this.classList.add('selected');
            userAnswers[currentQuestion] = parseInt(this.getAttribute('data-index'));
        });
    });
    
    // Add event listeners to navigation buttons
    if (currentQuestion > 0) {
        document.getElementById('prev-btn').addEventListener('click', prevQuestion);
    }
    document.getElementById('next-btn').addEventListener('click', nextQuestion);
    
    // Select previously selected option if exists
    if (userAnswers[currentQuestion] !== undefined) {
        const selectedOption = document.querySelector(`.option[data-index="${userAnswers[currentQuestion]}"]`);
        if (selectedOption) {
            selectedOption.classList.add('selected');
        }
    }
}

